// Regenerate __test__/snapshots/generate-native/*.{success,failure}.json by
// running each fixture through generate and writing the result.
//
// Run with:  pnpm regen-snapshots
//
// Storage layout:
//   - <fixture>.success.json    — summary + diagnostics + a path-only
//                                  artifacts list (no inline contents).
//   - <fixture-stem>/<path>      — each artifact's contents lives in a
//                                  sibling file. PR diffs read as real
//                                  TS instead of JSON-escaped strings.
//   - static-template.json       — path-only list for the angular static
//                                  templates that are byte-identical
//                                  across every success fixture.
//   - static-template/<path>     — sibling files for those templates.
//
// Both success and failure snapshots are regenerated. Use this script
// after any legitimate output change (emit format tweak, diagnostic
// message edit, planner reorder) — it is the single source of truth for
// the deep-equal snapshot tests in __test__/generate.snapshot.spec.ts.
//
// Snapshots that would NOT change are reported as "unchanged" and not
// rewritten — safe to run on a clean tree.

