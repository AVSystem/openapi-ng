let updated = 0;
let unchanged = 0;

function writeIfChanged(target, formatted) {
  const prev = fs.existsSync(target) ? fs.readFileSync(target, 'utf8') : '';
  if (prev === formatted) {
    unchanged += 1;
  } else {
    fs.mkdirSync(path.dirname(target), { recursive: true });
    fs.writeFileSync(target, formatted);
    updated += 1;
    console.log(`updated: ${path.relative(repoRoot, target)}`);
  }
}

/**
 * Write `result` as a path-only snapshot JSON plus a sibling directory
 * holding each non-static artifact's contents at `<siblingsDir>/<path>`.
 * Static-template paths are listed in the JSON but their contents are
 * pinned once under static-template/ (handled separately).
 */
function writeSuccessSnapshot(fixtureName, result) {
  const jsonPath = path.join(snapshotDir, `${fixtureName}.success.json`);
  const siblingsDir = path.join(snapshotDir, fixtureName);

  // Track which sibling files are still live; any leftover under
  // siblingsDir gets removed at the end so renames/deletes don't leave
  // orphans.
  const live = new Set();
  for (const a of result.artifacts) {
    if (STATIC_TEMPLATE_PATHS.has(a.path)) continue;
    const stripped = a.contents.replace(BANNER_RE, '');
    const target = path.join(siblingsDir, a.path);
    writeIfChanged(target, stripped);
    live.add(path.relative(siblingsDir, target));
  }
  removeOrphans(siblingsDir, live);

  const pathOnly = {
    summary: result.summary,
    diagnostics: result.diagnostics,
    artifacts: result.artifacts.map(a => ({ path: a.path })),
  };
  const formatted = JSON.stringify(pathOnly, null, 2) + '\n';
  writeIfChanged(jsonPath, formatted);
}

function writeStaticTemplate(result) {
  const live = new Set();
  for (const a of result.artifacts) {
    if (!STATIC_TEMPLATE_PATHS.has(a.path)) continue;
    const stripped = a.contents.replace(BANNER_RE, '');
    const target = path.join(STATIC_TEMPLATE_DIR, a.path);
    writeIfChanged(target, stripped);
    live.add(path.relative(STATIC_TEMPLATE_DIR, target));
  }
  removeOrphans(STATIC_TEMPLATE_DIR, live);

  // Sort for deterministic ordering across regen runs.
  const sortedPaths = [...STATIC_TEMPLATE_PATHS].sort();
  const index = { artifacts: sortedPaths.map(p => ({ path: p })) };
  const formatted = JSON.stringify(index, null, 2) + '\n';
  writeIfChanged(STATIC_TEMPLATE_INDEX, formatted);
}

function removeOrphans(dir, live) {
  if (!fs.existsSync(dir)) return;
  const walk = (current, prefix) => {
    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const child = path.join(current, entry.name);
      const rel = prefix ? path.join(prefix, entry.name) : entry.name;
      if (entry.isDirectory()) {
        walk(child, rel);
        // Remove empty intermediate directories left behind.
        if (fs.readdirSync(child).length === 0) {
          fs.rmdirSync(child);
        }
      } else if (!live.has(rel)) {
        fs.rmSync(child);
        console.log(`removed: ${path.relative(repoRoot, child)}`);
      }
    }
  };
  walk(dir, '');
}

let staticTemplateWritten = false;
for (const fixture of successFixtures) {
  const result = await generate({
    inputPath: path.join('test/fixtures', fixture),
    ...DEFAULT_OPTIONS,
  });
  if (!staticTemplateWritten) {
    writeStaticTemplate(result);
    staticTemplateWritten = true;
  }
  writeSuccessSnapshot(fixture, result);
}

for (const { fixture, snapshot } of failureFixtures) {
  let payload;
  try {
    await generate({
      inputPath: path.join('test/fixtures', fixture),
      ...DEFAULT_OPTIONS,
    });
    console.warn(`SKIP: ${fixture} succeeded — failure snapshot not regenerated`);
    continue;
  } catch (err) {
    payload = failurePayload(err);
  }
  const next = JSON.stringify(payload, null, 2) + '\n';
  writeIfChanged(path.join(snapshotDir, snapshot), next);
}

