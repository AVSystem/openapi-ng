const STATIC_TEMPLATE_DIR = path.join(snapshotDir, 'static-template');
const STATIC_TEMPLATE_INDEX = path.join(snapshotDir, 'static-template.json');

function failurePayload(err) {
  return {
    code: err.code,
    message: err.message,
    path: err.path ?? null,
    warnings: err.warnings ?? [],
  };
}

